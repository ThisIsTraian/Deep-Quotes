const quoteElement = document.getElementById('quote');
const authorElement = document.getElementById('author');
const topicsElement = document.getElementById('topic');
const mainContainerElement = document.getElementById('quote-container');
const button = document.getElementById('get-quote');

function showContainer() {// Show the quote container
    mainContainerElement.style.background = '#ffffff80';
    mainContainerElement.style.boxShadow = '0 2px 10px #0000001a';
}

button.addEventListener('click', () => {
    const randomQuote = quotes[Math.floor(Math.random() * quotes.length)];
    quoteElement.textContent = `“${randomQuote.text}”`;
    authorElement.textContent = `${randomQuote.author} (${randomQuote.profession.toLowerCase()})`;
    topicsElement.textContent = randomQuote.topics.map(topic => `#${topic.toLowerCase()}`).join(' ');
    showContainer();
});

quoteElement.addEventListener('click', () => {
    const quoteText = quoteElement.textContent;
    const googleSearchUrl = `https://www.google.com/search?q=${encodeURIComponent(quoteText)}`;
    window.open(googleSearchUrl, '_blank'); //New Tab
});
